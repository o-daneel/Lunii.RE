void print_buffer(char* buffer, int size) {
    for (int i=0; i<size; i++) {
        if (i % 32 == 0)
            printf("\n> %.8X : ", i); 
        printf("%.2X ", buffer[i] & 0xFF); 
    }
    printf("\n"); 
}

